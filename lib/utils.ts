// Utility functions
export function formatCurrency(amount: number, currency: string = 'USD'): string {
  return new Intl.NumberFormat('en-US', {
    style: 'currency',
    currency: currency,
  }).format(amount);
}

export function formatDate(dateString: string): string {
  return new Date(dateString).toLocaleDateString('en-US', {
    year: 'numeric',
    month: 'long',
    day: 'numeric',
  });
}

export function generateTripSummary(data: { flights: any[]; lodges: any[]; people: any[] }): string {
  const { flights, lodges, people } = data;
  const parts = [];
  
  if (flights.length > 0) {
    parts.push(`${flights.length} flight${flights.length > 1 ? 's' : ''}`);
  }
  if (lodges.length > 0) {
    parts.push(`${lodges.length} accommodation${lodges.length > 1 ? 's' : ''}`);
  }
  if (people.length > 0) {
    parts.push(`${people.length} traveler${people.length > 1 ? 's' : ''}`);
  }
  
  return parts.length > 0 ? parts.join(', ') : 'No details added yet';
}